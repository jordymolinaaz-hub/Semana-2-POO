# Declaración de variables
edad = 20  # Entero: edad de la persona

estatura = 1.65  # Flotante: estatura en metros
nombre = "Omar Molina"  # Cadena de texto
es_estudiante = True  # Booleano: indica si es estudiante

# Operaciones con las variables
nueva_edad = edad + 5  # Se suma 5 a la edad actual
nueva_estatura = estatura * 2  # Se duplica la estatura
saludo = "Hola " + nombre  # Se crea un saludo concatenando el texto y el nombre
puede_ingresar = edad >= 18 and es_estudiante  # Solo puede ingresar si es mayor de edad y es estudiante

# Mostrar los resultados
print("Nombre:", nombre)
print("Edad:", edad)
print("Edad dentro de 5 años:", nueva_edad)
print("Estatura actual:", estatura)
print("Estatura duplicada:", nueva_estatura)
print("Saludo:", saludo)
print("¿Puede ingresar?:", puede_ingresar)

